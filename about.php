          <p>And how the prosaic political-science point (I know, IkeaOrDeath didn't preach at you, but frankly parties are a bit more important than pressboard lampstands).
          This isn't just about party and song names but about how our politics is changing.  Organizations in all walks of life seem less-rooted, more flexible, more apt to change.  And boundaries--between genres, between substance and self-promotion, between fiction and reality--seem ever more porous.  This may just be an illusion--every generation in modernity sees itself as moving faster than its predecessors--but there are good reasons to think that things <em>are</em> changing  (See Clay Shirkey's, "Here Comes Everybody", <a href="http://www.theguardian.com/books/2008/mar/22/society1">http://www.theguardian.com/books/2008/mar/22/society1</a>, and Moises Naim's, "The End of Power", <a href="http://bit.ly/naim-review-lse">http://bit.ly/naim-review-lse</a>)</p>

          <p>If so, the questions facing of citizens becomes much more critical and difficult.  How do we decide between compromised experience and unsullied novelty?  How do we hold politicians accountable if they themselves really don't expect to be around by the next election?</p>
          
